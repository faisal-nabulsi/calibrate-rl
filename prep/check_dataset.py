"""
Independent dataset integrity checker for the skeleton datasets.

The skeleton golds are GENERATED BY CODE, so a generator bug = training on wrong
answers, and no downstream grader/calibration rigor can recover from it. Step 1
("verify the golds") only spot-checked 10 instances; this checks the whole set.

Two layers:

  A. STRUCTURAL (model-free, 100% reliable, all concepts):
     - duplicate rows
     - CONFLICTING golds: same problem text carrying two different answers
       (this is how the continued_fraction bug was found)
     - non-numeric / missing golds

  B. INDEPENDENT RECOMPUTATION (parseable concepts only):
     re-derive the answer from the PROBLEM TEXT and compare to the stored gold.
     Concepts with no recomputer are reported as UNCHECKED (honest coverage).

Reading the output:
  - A concept with a SMALL mismatch fraction => real bad golds in those rows.
  - A concept at ~100% mismatch => almost certainly MY parser/formula is wrong,
    not the data; treat as "checker suspect", not a data bug.

Usage:
    python3 check_dataset.py [data/skeleton_dataset_v10.json]
"""
import sys, re, json
from collections import defaultdict, Counter
from fractions import Fraction
from math import gcd, isqrt
from itertools import combinations


# ── small number-theory helpers ───────────────────────────────────────────────
def divisors(n):
    ds = []
    for d in range(1, isqrt(n) + 1):
        if n % d == 0:
            ds += [d, n // d]
    return sorted(set(ds))

def num_divisors(n):
    return len(divisors(n))

def ints(s):
    return [int(x) for x in re.findall(r"-?\d+", s)]


# ── per-concept recomputers ──────────────────────────────────────────────────
# Each returns the correct answer (int/float) or None if it can't parse.
def rc_continued_fraction(p):
    # "X+1/(X+1/X)" or deeper; base X repeated 'depth' times. Read X and depth.
    m = re.search(r"value of\s*(\d+)\+1/", p)
    if not m: return None
    x = int(m.group(1))
    depth = p.count(f"{x}+1/") + 1     # each "x+1/" plus the innermost x
    v = Fraction(x)
    for _ in range(depth - 1):
        v = x + 1 / v
    v = Fraction(v)
    return v.numerator + v.denominator

def rc_custom_binary_op(p):
    # a⊕b = a+b+ab, left-folded over ALL operands. v10 had 3 operands
    # ((a⊕b)⊕c); v11 uses 4 (((a⊕b)⊕c)⊕d) — fold all of them, not the last 3.
    # The operator definition ("x+y+xy") carries no digits, so every integer in
    # the text is an operand.
    nums = ints(p)
    if len(nums) < 2: return None
    op = lambda u, v: u + v + u * v
    v = nums[0]
    for x in nums[1:]:
        v = op(v, x)
    return v

def rc_modular_exponent(p):
    m = re.search(r"(\d+)\^?(\d+)?.*?divided by\s*(\d+)", p)
    n = ints(p)
    if len(n) < 3: return None
    base, exp, mod = n[0], n[1], n[2]
    return pow(base, exp, mod)

def rc_constrained_divisor_count(p):
    A = re.search(r"divisors of (\d+)", p)
    if not A: return None
    ds = divisors(int(A.group(1)))
    if "odd" in p:  return sum(1 for d in ds if d % 2 == 1)
    if "even" in p: return sum(1 for d in ds if d % 2 == 0)
    gt = re.search(r"greater than (\d+)", p)
    lt = re.search(r"less than (\d+)", p)
    if gt: return sum(1 for d in ds if d > int(gt.group(1)))
    if lt: return sum(1 for d in ds if d < int(lt.group(1)))
    return None

def rc_divisor_sum_filter(p):
    n = ints(p)
    if not n: return None
    x = n[-1]
    odd = "odd" in p.lower()
    return sum(d for d in divisors(x) if (d % 2 == 1) == odd)

def rc_perfect_square_divisible(p):
    n = ints(p)
    if len(n) < 2: return None
    L, M = n[0], n[1]
    return sum(1 for k in range(1, isqrt(L) + 1) if (k * k) % M == 0 and k * k < L)

def rc_multi_constraint_square(p):
    n = ints(p)
    if len(n) < 3: return None
    L, M, d = n[0], n[1], n[2]
    return sum(1 for k in range(1, isqrt(L) + 1)
               if k * k < L and (k * k) % M == 0 and (k * k) % 10 == d)

def rc_triangular_filter_count(p):
    L = re.search(r"(?:below|less than|under) (\d+)", p)
    M = re.search(r"(?:multiples of|divisible by) (\d+)", p)
    if not (L and M): return None
    L, M = int(L.group(1)), int(M.group(1))
    out, k = 0, 1
    while k * (k + 1) // 2 < L:
        if (k * (k + 1) // 2) % M == 0: out += 1
        k += 1
    return out

def rc_arith_term_filter(p):
    N = re.search(r"first (\d+) terms", p)
    M = re.search(r"(?:multiples of|divisible by) (\d+)", p)
    if not (N and M): return None
    N, M = int(N.group(1)), int(M.group(1))
    a = d = None
    for pat in [r"first term (\d+) and common difference (\d+)",
                r"first term (\d+),?\s*(?:common )?difference (\d+)",
                r"starts at (\d+) with common difference (\d+)",
                r"beginning (\d+) and increasing by (\d+)"]:
        m = re.search(pat, p)
        if m: a, d = int(m.group(1)), int(m.group(2)); break
    if a is None:                                  # "sequence A, A2, A3, ..."
        m = re.search(r"sequence (\d+),\s*(\d+)", p)
        if m: a, d = int(m.group(1)), int(m.group(2)) - int(m.group(1))
    if a is None: return None
    return sum(1 for i in range(N) if (a + i * d) % M == 0)

def rc_lattice_points_circle(p):
    # bound after <= ; "N²"/"N^2" -> square it, plain "N" -> use directly
    m = re.search(r"[≤<]=?\s*(\d+)\s*(²|\^2)?", p)
    if not m: return None
    base = int(m.group(1))
    bound = base * base if m.group(2) else base
    R = isqrt(bound)
    return sum(1 for x in range(-R, R + 1) for y in range(-R, R + 1) if x * x + y * y <= bound)

def rc_ordered_triple_constraint(p):
    m = (re.search(r"a\+b\+c\s*=\s*(\d+)", p) or re.search(r"sum to (\d+)", p)
         or re.search(r"can (\d+) be written as a\+b\+c", p)
         or re.search(r"(\d+) be written as a\+b\+c", p))
    if not m: return None
    S = int(m.group(1))
    return sum(1 for a, b, c in combinations(range(0, S + 1), 3) if a + b + c == S)

def rc_alternating_cubes(p):
    n = ints(p)
    if not n: return None
    top = max(n)
    if top % 2: top -= 1
    return sum((2 * k) ** 3 - (2 * k - 1) ** 3 for k in range(1, top // 2 + 1))

def rc_complex_eq_solcount(p):
    m = re.search(r"z\^?(\d+)\s*=", p)
    if not m: return None
    P = int(m.group(1))
    return P + 2                      # r=1: z^(P+1)=1 -> P+1 roots; plus z=0

def rc_roots_of_unity_sum(p):
    ex = re.findall(r"z\^(\d+)\s*=\s*1", p)
    if len(ex) >= 2:
        return gcd(int(ex[0]), int(ex[1]))
    ords = re.findall(r"(\d+)(?:th|st|nd|rd)", p)   # "6th and 24th root of unity"
    if len(ords) >= 2:
        return gcd(int(ords[0]), int(ords[1]))
    return None

def rc_inclusion_exclusion_3set(p):
    N = re.search(r"to (\d+)", p)
    divs = re.search(r"divisible by (\d+),\s*(\d+),?\s*(?:or|and)?\s*(\d+)", p)
    if not (N and divs): return None
    N = int(N.group(1)); a, b, c = (int(divs.group(i)) for i in (1, 2, 3))
    return sum(1 for x in range(1, N + 1) if x % a == 0 or x % b == 0 or x % c == 0)

def rc_telescoping_mn(p):
    D = re.search(r"k\s*\(\s*k\s*\+\s*(\d+)\s*\)", p)
    N = re.search(r"k\s*=\s*1\s*(?:\.\.|to|…|,\s*\.\.\.,)\s*(\d+)", p) or \
        re.search(r"for k\s*=\s*1[^\d]{1,6}(\d+)", p)
    if not (D and N): return None
    D, N = int(D.group(1)), int(N.group(1))
    s = sum(Fraction(1, k * (k + D)) for k in range(1, N + 1))
    return s.numerator + s.denominator

def rc_constrained_digit_count(p):
    m = (re.search(r"between (\d+) and (\d+)", p) or re.search(r"\[(\d+),\s*(\d+)\]", p)
         or re.search(r"from (\d+) to (\d+)", p))
    s = re.search(r"digit sum (?:of|is|equal to|to) (\d+)", p) or \
        re.search(r"digits sum to (\d+)", p) or \
        re.search(r"sum of (?:the )?digits (?:is |of |equal to )?(\d+)", p)
    if not (m and s): return None
    A, B, S = int(m.group(1)), int(m.group(2)), int(s.group(1))
    return sum(1 for v in range(A, B + 1) if sum(int(c) for c in str(v)) == S)

def rc_count_pythagorean(p):
    n = ints(p)
    if not n: return None
    C = n[-1]
    cnt = 0
    for a in range(1, C + 1):
        for b in range(a, C + 1):
            c2 = a * a + b * b
            c = isqrt(c2)
            if c * c == c2 and c <= C:
                cnt += 1
    return cnt

def rc_complement_prob_mn(p):
    m = re.search(r"(\d+)-sided", p) or re.search(r"(\d+)-faced", p)
    if not m: return None
    faces = int(m.group(1))
    th = re.search(r"(\d+)/(\d+)", p)      # threshold varies across instances (1/2, 3/4, ...)
    thr = Fraction(int(th.group(1)), int(th.group(2))) if th else Fraction(1, 2)
    k = 1                                  # min rolls so P(>=1 of a face) > thr
    while 1 - (Fraction(faces - 1, faces)) ** k <= thr:
        k += 1
    return k

def rc_complex_modulus_power(p):
    n = ints(p)
    if not n: return None
    V = max(n)
    tot = 0
    for a in range(1, isqrt(V) + 1):
        for b in range(a, isqrt(V) + 1):
            if a * a + b * b == V:
                tot += a + b
    return tot

def rc_prime_power_divisors(p):
    n = ints(p)
    if not n: return None
    D = n[-1]
    k = 1
    while num_divisors(k) != D:
        k += 1
    return k

def rc_box_diagonal_sq(p):
    m = re.search(r"exactly (\d+)", p)
    want = int(m.group(1)) if m else 4
    edges, k = [], 2
    while len(edges) < 3:
        if num_divisors(k) == want: edges.append(k)
        k += 1
    return sum(e * e for e in edges)

def rc_lcm_gcd_system(p):
    n = ints(p)
    if len(n) < 4: return None
    A, L, B, G = n[0], n[1], n[2], n[3]   # lcm(n,A)=L, gcd(n,B)=G, smallest n
    for x in range(1, L + 1):
        if (A * x) // gcd(A, x) == L and gcd(x, B) == G:
            return x
    return None


def rc_poly_remainder(p):
    # "...ax³ +bx² +cx +d = V (or 'equal V')..." find positive integer x with poly(x)=V
    s = p.replace("^3", "³").replace("^2", "²").replace(" ", "")
    parts = re.split(r"=|equals?", s)
    if len(parts) < 2:
        return None
    lhs, rhs = parts[0], parts[-1]
    Am = re.search(r"(-?\d+)x³", lhs)
    Bm = re.search(r"(-?\d+)x²", lhs)
    Cm = re.search(r"(-?\d+)x(?![²³\d])", lhs)
    Vm = re.search(r"-?\d+", rhs)
    if not (Am and Bm and Cm and Vm):
        return None
    A, B, C, V = int(Am.group(1)), int(Bm.group(1)), int(Cm.group(1)), int(Vm.group())
    rem = re.sub(r"-?\d+x³", "", lhs)
    rem = re.sub(r"-?\d+x²", "", rem)
    rem = re.sub(r"-?\d+x", "", rem)
    Dm = re.search(r"-?\d+", rem)
    D = int(Dm.group()) if Dm else 0
    for x in range(1, 3000):
        if A * x**3 + B * x * x + C * x + D == V:
            return x
    return None


def rc_polynomial_sign_intervals(p):
    # roots at 1..k with given multiplicities; count open intervals where the
    # product ∏(x-i)^m_i is positive. Sign flips only across ODD-multiplicity roots.
    m = re.search(r"\[([\d,\s]+)\]", p)
    if m:
        mult = [int(x) for x in m.group(1).split(",")]
    else:
        exps = re.findall(r"\)\s*\^\s*\(?\s*(\d+)", p)   # (x-i)^m form
        if not exps:
            return None
        mult = [int(x) for x in exps]
    k = len(mult)
    pos = 0
    for t in (r + 0.5 for r in range(0, k + 1)):        # one test point per interval
        sign = 1
        for root, mu in zip(range(1, k + 1), mult):
            if mu % 2 == 1 and t < root:
                sign = -sign
        if sign > 0:
            pos += 1
    return pos


def _solve3(A, b):
    from fractions import Fraction
    M = [[Fraction(v) for v in row] + [Fraction(bb)] for row, bb in zip(A, b)]
    for i in range(3):
        piv = next((r for r in range(i, 3) if M[r][i] != 0), None)
        if piv is None:
            return None
        M[i], M[piv] = M[piv], M[i]
        inv = M[i][i]
        M[i] = [v / inv for v in M[i]]
        for r in range(3):
            if r != i and M[r][i] != 0:
                f = M[r][i]
                M[r] = [a - f * b for a, b in zip(M[r], M[i])]
    return [M[r][3] for r in range(3)]


def rc_algebraic_system_2eq(p):
    # 3 eqs "ax+by+cz=d"; question always asks x+y+z.
    eqs = re.findall(r"(-?\d+)x([+-]\d+)y([+-]\d+)z=(-?\d+)", p.replace(" ", ""))
    if len(eqs) < 3:
        return None
    A = [[int(a), int(b), int(c)] for a, b, c, _ in eqs[:3]]
    rhs = [int(d) for *_, d in eqs[:3]]
    sol = _solve3(A, rhs)
    if sol is None:
        return None
    total = sum(sol)
    return int(total) if total == int(total) else None


def rc_constrained_subset_count(p):
    # "k-element subsets of {1..N} with sum ≡ r (mod m)"
    km = re.search(r"(\d+)-element", p) or re.search(r"size-?\s?(\d+)", p)
    nm = (re.search(r"\{1[^}]*?(\d+)\}", p) or re.search(r"first (\d+) positive", p)
          or re.search(r"\{1\.\.(\d+)\}", p))
    rm = (re.search(r"remainder (\d+) when divided by (\d+)", p)
          or re.search(r"≡\s*(\d+)\s*\(?mod\s*(\d+)", p)
          or re.search(r"(\d+)\s*more than a multiple of (\d+)", p))
    if not (km and nm and rm):
        return None
    k, N, r, mod = int(km.group(1)), int(nm.group(1)), int(rm.group(1)), int(rm.group(2))
    return sum(1 for c in combinations(range(1, N + 1), k) if sum(c) % mod == r % mod)


def rc_equalization_fraction(p):
    # k full glasses + 1 partial at fill f; pour x from each full glass to equalize:
    # x = (1-f)/(k+1); answer = m+n of the reduced fraction x.
    m = (re.search(r"first (\d+) are full", p) or re.search(r"(\d+) completely full", p)
         or re.search(r"(\d+) full", p))
    fm = (re.search(r"(\d+)/(\d+) full", p) or re.search(r"at (\d+)/(\d+)", p)
          or re.search(r"last is (\d+)/(\d+)", p) or re.search(r"one (\d+)/(\d+)", p))
    if not (m and fm):
        return None
    k = int(m.group(1)); f = Fraction(int(fm.group(1)), int(fm.group(2)))
    x = (1 - f) / (k + 1)
    return x.numerator + x.denominator


def _pow_exp(val, B):
    # exact integer log_B(val): exponent if val is a power of B, else None
    if val <= 0 or B <= 1:
        return None
    e = 0
    while val % B == 0:
        val //= B; e += 1
    return e if val == 1 else None

def _log_arg(arg, B):
    # one log argument: a number, a power "B^e", or a product/quotient of them
    arg = arg.replace("×", "·").replace("*", "·")
    total, sign = 0, 1
    for tok in re.split(r"([·/])", arg):
        tok = tok.strip()
        if tok == "·": sign = 1
        elif tok == "/": sign = -1
        elif tok:
            mm = re.match(r"(\d+)\^(\d+)$", tok)
            val = int(mm.group(1)) ** int(mm.group(2)) if mm else (int(tok) if tok.isdigit() else None)
            if val is None: return None
            e = _pow_exp(val, B)
            if e is None: return None
            total += sign * e; sign = 1
    return total

def rc_log_laws(p):
    # every argument is a power of the base; answer = signed sum of exponents
    base = re.search(r"log[_\s]*(?:base\s*)?\{?(\d+)\}?", p)
    if not base:
        return None
    B = int(base.group(1))
    pn = p.replace("plus", "+").replace("minus", "-")
    terms = re.findall(r"([+\-]?)\s*log[_\s]*(?:base\s*)?\{?\d+\}?\s*(?:of\s+(\d+)|\(([^)]*)\))", pn)
    if not terms:
        return None
    tot = 0
    for s, ofnum, paren in terms:
        e = _log_arg(ofnum or paren, B)
        if e is None: return None
        tot += -e if s == "-" else e
    return tot


def rc_chain_log_laws__ordered_triple_constraint(p):
    # depth-1 composite: the triple-sum target is embedded as
    # log_B(B^e1 · B^e2 / B^e3). Recover N via _log_arg (validates every factor
    # is an exact power of B), then count triples 0≤a<b<c with a+b+c=N — same
    # brute force as rc_ordered_triple_constraint, but N comes from the log,
    # never from a bare integer in the text.
    m = re.search(r"log[_\s]*\{?(\d+)\}?\s*\(([^)]*)\)", p)
    if not m: return None
    N = _log_arg(m.group(2), int(m.group(1)))
    if N is None or N < 0: return None
    return sum(1 for a, b, c in combinations(range(0, N + 1), 3) if a + b + c == N)


def _cdc_from_text(p, N):
    # shared: count divisors of N per the embedded cdc condition (odd / >t / <t).
    # N comes from the composite's A-step (computed below), never a bare integer.
    if N is None or N < 1:
        return None
    ds = divisors(N)
    if "odd" in p:
        return sum(1 for d in ds if d % 2 == 1)
    gt = re.search(r"greater than (\d+)", p)
    lt = re.search(r"less than (\d+)", p)
    if gt:
        return sum(1 for d in ds if d > int(gt.group(1)))
    if lt:
        return sum(1 for d in ds if d < int(lt.group(1)))
    return None

def rc_chain_prime_power_divisors__constrained_divisor_count(p):
    # depth-1: A=ppd embeds "smallest int with exactly D positive divisors"; recover D,
    # recompute N (smallest with D divisors) independently, then count per the cdc clause.
    m = re.search(r"exactly (\d+) positive divisors", p)
    if not m:
        return None
    D = int(m.group(1))
    N = 1
    while num_divisors(N) != D:
        N += 1
        if N > 10**6:          # bounded (mirrors the generator guard); awkward D -> unverifiable
            return None
    return _cdc_from_text(p, N)

def rc_chain_constrained_divisor_count__modular_exponent(p):
    # depth-1: A=cdc embeds "divisors of NUM that are {odd|greater than t|less than t}" -> e;
    # recompute e independently, then B=modexp: ans = a^e mod m (exponent is the symbolic "e").
    A = re.search(r"divisors of (\d+)", p)
    if not A:
        return None
    ds = divisors(int(A.group(1)))
    if "odd" in p:
        e = sum(1 for d in ds if d % 2 == 1)
    else:
        gt = re.search(r"greater than (\d+)", p)
        lt = re.search(r"less than (\d+)", p)
        if gt:
            e = sum(1 for d in ds if d > int(gt.group(1)))
        elif lt:
            e = sum(1 for d in ds if d < int(lt.group(1)))
        else:
            return None
    am = re.search(r"(\d+)\s*\^\s*e\b", p)              # base a (anchored on ^e)
    mm = re.search(r"(?:divided by|mod)\s+(\d+)", p)    # modulus m
    if not (am and mm):
        return None
    return pow(int(am.group(1)), e, int(mm.group(1)))



    print(f"# {path}\nrows: {len(rows)}")

    # ── A. structural ─────────────────────────────────────────────────────────
    by_prob = defaultdict(set)
    for r in rows:
        by_prob[r["problem"]].add(str(r.get("answer")))
    conflicts = {p: a for p, a in by_prob.items() if len(a) > 1}
    exact = Counter((r["problem"], str(r.get("answer"))) for r in rows)
    dup_rows = sum(c - 1 for c in exact.values() if c > 1)
    nonnum = [r for r in rows if not re.fullmatch(r"-?\d+(?:/\d+|\.\d+)?", str(r.get("answer")).strip())]

    print(f"\n[A] STRUCTURAL")
    print(f"  unique problems     : {len(by_prob)}")
    print(f"  exact-duplicate rows: {dup_rows}")
    print(f"  non-numeric golds   : {len(nonnum)}")
    print(f"  CONFLICTING golds   : {len(conflicts)} problems (same text, 2+ golds)")
    for p, a in list(conflicts.items())[:12]:
        c = next(r.get("skeleton_type") for r in rows if r["problem"] == p)
        print(f"     [{c}] golds={sorted(a)} :: {p[:70]}")

    # ── B. recomputation ──────────────────────────────────────────────────────
    print(f"\n[B] INDEPENDENT RECOMPUTATION (per concept)")
    by_concept = defaultdict(list)
    for r in rows:
        by_concept[r["skeleton_type"]].append(r)

    bad_examples = []
    total_checked = total_bad = 0
    print(f"  {'concept':28} {'rows':>5} {'checked':>7} {'mismatch':>8}")
    for c in sorted(by_concept):
        rs = by_concept[c]
        fn = RECOMPUTERS.get(c)
        if not fn:
            print(f"  {c:28} {len(rs):5d} {'-':>7} {'(no checker)':>8}")
            continue
        checked = bad = 0
        for r in rs:
            try:
                got = fn(r["problem"])
            except Exception:
                got = None
            if got is None:
                continue
            checked += 1
            try:
                stored = float(Fraction(str(r["answer"])))
            except Exception:
                stored = None
            if stored is None or abs(float(got) - stored) > 1e-6:
                bad += 1
                if len(bad_examples) < 25:
                    bad_examples.append((c, r["answer"], got, r["problem"][:66]))
        total_checked += checked; total_bad += bad
        rate = f"{bad}/{checked}" if checked else "0/0"
        flag = "  <-- CHECKER SUSPECT" if checked and bad / checked > 0.5 else ("  <-- BAD GOLDS" if bad else "")
        print(f"  {c:28} {len(rs):5d} {checked:7d} {rate:>8}{flag}")

    print(f"\n  TOTAL: {total_bad} mismatches over {total_checked} recomputed rows")
    if bad_examples:
        print(f"\n  Example mismatches (stored gold vs recomputed):")
        for c, stored, got, p in bad_examples:
            print(f"    [{c}] stored={stored} correct={got} :: {p}")


